public class MathApp{

	public static double multiplication(String operation, double number, double numberTwo){

	if(operation == "*"){
	return number * numberTwo;}
	else return 0.0;

}

	public static double subtraction(String operation, double number, double numberTwo){

	if(operation == "-"){ 
	return number - numberTwo;}
	else return 0.0;

}


	public static double addition(String operation, double number, double numberTwo){

	if(operation == "+"){
	return number + numberTwo;}
	else return 0.0;

}

	public static double division(String operation, double number, double numberTwo){

	if(operation == "/"){ 
	return number / numberTwo;}
	else return 0.0;

}


	

}
